# -*- coding:utf-8

import turtle as t

#삼각형 그리기

t.forward(100) #거북이가 앞으로 100 이동한다.
t.left(120) #거북이가 왼쪽으로 120도 회전한다.
#이 같은 과정을 3번 반복 한다.
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)

#사각형 그리기

t.forward(100) #거북이가 앞으로 100 이동한다.
t.left(90) #거북이가 왼쪽으로 90도 회전한다.
# 이 같은 과정을 4번 반복 한다.
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)

#원 그리기

t.circle(50) # 반지름 50 짜리 원을 그린다.

